    private void hitung1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitung1ActionPerformed
        Segitiga s = new Segitiga();
        String optionLuas = cbLuas.getSelectedItem().toString();
        String optionKeliling = cbKeliling.getSelectedItem().toString();
        double a = Double.parseDouble(sisiA.getText());
        double b = Double.parseDouble(sisiB.getText());
        double c = Double.parseDouble(alas.getText());
        double t = Double.parseDouble(tinggi.getText());
        double luas = s.setLuas(c, t);
        double keliling = s.setKeliling(a, b, c);

        switch (optionLuas){
            case "cm¬≤":
            luas = s.setLuas(a, t);
            break;
            case "mm¬≤":
            luas = s.setLuas(a, t)*100;
            break;
            case "dm¬≤":
            luas = s.setLuas(a, t)/100;
            break;
            case "m¬≤":
            luas = s.setLuas(a, t)/10000;
            break;
        }
        switch (optionKeliling){
            case "cm":
            keliling = s.setKeliling(a, b, c);
            break;
            case "mm":
            keliling = s.setKeliling(a, b, c)*10;
            break;
            case "dm":
            keliling = s.setKeliling(a, b, c)/10;
            break;
            case "m":
            keliling = s.setKeliling(a, b, c)/100;
            break;
        }
        luasSegitiga.setText(new String().valueOf(luas));
        kelilingSegitiga.setText(new String().valueOf(keliling));
    }//GEN-LAST:event_hitung1ActionPerformed

