    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        SegitigaSiku ss = new SegitigaSiku();
        String optionLuas = cbLuas.getSelectedItem().toString();
        String optionKeliling = cbKeliling.getSelectedItem().toString();
        double a = Double.parseDouble(sisiA.getText());
        double b = Double.parseDouble(sisiB.getText());
        double c = ss.setSisiMiring(a, b);
        double luas = ss.setLuas(a, b);
        double keliling = ss.setKeliling(a, b, c);

        switch (optionLuas){
            case "cm¬≤":
            luas = ss.setLuas(a,b);
            break;
            case "mm¬≤":
            luas = ss.setLuas(a,b)*100;
            break;
            case "dm¬≤":
            luas = ss.setLuas(a,b)/100;
            break;
            case "m¬≤":
            luas = ss.setLuas(a,b)/10000;
            break;
        }
        switch (optionKeliling){
            case "cm":
            keliling = ss.setKeliling(a,b,c);
            break;
            case "mm":
            keliling = ss.setKeliling(a,b,c)*10;
            break;
            case "dm":
            keliling = ss.setKeliling(a,b,c)/10;
            break;
            case "m":
            keliling = ss.setKeliling(a,b,c)/100;
            break;
        }
        sisiC.setText(new String(). valueOf(c));
        luasSegitigaSiku.setText(new String().valueOf(luas));
        kelilingSegitigaSiku.setText(new String().valueOf(keliling));
    }//GEN-LAST:event_hitungActionPerformed

